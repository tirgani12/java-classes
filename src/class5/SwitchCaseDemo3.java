package class5;

public class SwitchCaseDemo3 {
    public static void main(String[] args) {
        String day="Friday";
        switch (day){
            case "monday":
                System.out.println("1");
                break;
            case "Tuesday":
                System.out.println("2");
                break;
            default:
                System.out.println("wrong day");
        }
    }
}
