package class6;

public class WhileLoop2 {
    public static void main(String[] args) {
        int number = 1;
        while (number < 5) {
            System.out.println(number);
            number++;
        }
        int num = 0;
        while (num < 10) {
            System.out.println(num);
            num += 2;
        }
        int num1 = 1;
        while (num1 <= 100) {
            System.out.println(num1);
            num1++;
        }
        int num2 = 1;
        while (num2 <= 15) {
            System.out.println(num2);
            num2 += 2;
        }
        int num3 = 0;
        while (num3 <= 50) {
            System.out.println(num3);
            num3 += 5;
        }
        int num4 = 10;
        while (num4 > 0) {
            System.out.println(num4);
            num4--;
        }
    }
}